# membuat latihan konversi saatuan suhu

# program konversi celcius ke satuan lain
print("\nPROGRAM KONVERSI SATUAN CELCIUS\n")

# form input
celcius = float(input('masukan suhu celcius:'))
print('\nsuhu adalah', celcius, 'Celcius')

# reamur
reamur = (4/5) * celcius
print('\nsuhu adalah', reamur, 'Reamur')

# fahrenheit
fh = ((9/5) * celcius)+32
print('\nsuhu adalah', fh, 'Farenheit')

# kelvin
kelvin = celcius + 273
print('\nsuhu adalah', kelvin, 'Farenheit')


