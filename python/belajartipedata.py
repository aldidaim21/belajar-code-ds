# tipe data angka satuan (integer)
dataint = 1
print('data :', dataint, 'bertipe', type(dataint))

# tipe data desimal (float)
dataflt = 0.4
print('data :', dataflt, 'bertipe', type(dataflt))

# tipe data kumpulan karakter (string)
datastr = 'asep'
print('data :', datastr, 'bertipe', type(datastr))

# tipe data biner true false (boolean)
databl = True
print('data :', databl, 'bertipe', type(databl))

# bilangan data komplex
datacpl = complex(5, 6)
print('data :', datacpl, 'bertipe', type(datacpl))
